    &nbsp;&nbsp;&nbsp;&nbsp;II - quando a matéria exigir, para sua aprovação, 
    o voto favorável de dois terços ou de maioria absoluta dos membros da Câmara 
    Municipal;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;III - nos casos de empate; <BR>
    &nbsp;&nbsp;&nbsp;&nbsp;IV - nas votações secretas.<BR>
    <BR>
    Art. 23. A sessão legislativa anual desenvolve-se de 1º de março a 30 de junho 
    e de 1º de agosto a 20 de dezembro, independentemente de convocação. (Redação 
    dada pela Emenda nº 09, de 27.12.2001)<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;Parágrafo único. A Câmara Municipal reunir-se-á em 
    sessões ordinárias, extraordinárias, solenes e secretas, nos termos do Regimento 
    Interno. <BR>
    <BR>
    Art. 24. A iniciativa da convocação de sessão extraordinária da Câmara cabe 
    ao seu Presidente, a um terço de seus membros, à Comissão Representativa ou 
    ao Prefeito Municipal.<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;§ 1º Nas sessões extraordinárias, a Câmara só pode 
    deliberar sobre matéria da convocação. <BR>
