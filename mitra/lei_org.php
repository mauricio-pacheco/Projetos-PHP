    a poluição e a erosão em qualquer de suas formas;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;II - fiscalizar e normatizar 
    a produção, o armazenamento, o transporte, o uso e o destino final de produtos, 
    embalagens e substâncias potencialmente perigosas à saúde e aos recursos naturais;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;III - promover a educação 
    ambiental em todos os níveis de ensino e a conscientização pública para a 
    proteção do meio ambiente;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;IV - denunciar a pesca e a 
    caça predatória;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;V - denunciar o lançamento 
