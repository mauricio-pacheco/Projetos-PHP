    da Câmara, ausentar-se do Município por mais de dez dias no Estado, e de cinco 
    dias fora do Estado, sob pena de perda de mandato. (Redação dada pele Emenda 
    nº 10, de 27.11.2002) <BR>
    <BR>
    Art. 49. O Prefeito poderá licenciar-se por motivos de doença, devidamente 
    comprovada, ou para ausentar-se em missão oficial, sem prejuízo da remuneração. 
    <BR>
    <BR>
    Art. 50. O Prefeito pode gozar férias anuais de trinta dias, sem prejuízo 
    do subsídio, devendo comunicar à Câmara o período em que vai gozá-las. (Redação 
    dada pele Emenda nº 10, de 27.11.2002) <BR>
