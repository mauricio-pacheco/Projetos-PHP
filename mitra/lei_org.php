    suplente através do Presidente da Câmara, na forma do Regimento Interno.<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;§ 1º O suplente convocado deverá tomar posse dentro 
    do prazo de quinze dias, salvo motivo justo, aceito pela Câmara, sob pena 
    de ser considerado renunciante. <BR>
    &nbsp;&nbsp;&nbsp;&nbsp;§ 2º Ocorrendo vaga e não havendo suplente para assumir, 
