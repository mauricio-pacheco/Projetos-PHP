    direitos ou interesses do Município, sujeitos à Administração Municipal;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;XI - ausentar-se do Município, por tempo superior 
    ao previsto nesta lei, ou afastar-se do Município, sem autorização legislativa, 
    nos casos exigidos em lei;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;XII - iniciar investimento sem as cautelas previstas 
    no art. 109, inciso II desta Lei;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;XIII - proceder de modo incompatível com a dignidade 
    e decoro do cargo. <BR>
