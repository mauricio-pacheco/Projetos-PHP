    monopolística e ao aumento abusivo de lucros.<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;§ 2º O município poderá revogar 
    a concessão ou a permissão dos serviços que forem executados em desconformidade 
    com o contrato ou ato pertinente, bem como daqueles que se revelarem manifestamente 
    insatisfatórios para o atendimento dos usuários. <BR>
