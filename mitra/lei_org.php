  <p align="justify"><font size="1" face="Verdana, Arial, Helvetica, sans-serif"> Art. 36. Os Vereadores não poderão: <BR>
    &nbsp;&nbsp;&nbsp;&nbsp;I - desde a expedição do diploma:<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;a) firmar ou manter contrato 
    com pessoa jurídica de direito público, autarquias, empresas públicas, sociedades 
    de economia mista, fundações ou empresas concessionárias de serviço público, 
    salvo quando o contrato obedecer a cláusulas uniformes; <BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;b) aceitar ou exercer cargo, 
    função ou emprego remunerado, inclusive os que sejam demissíveis "ad nutum", 
    nas entidades constantes na alínea anterior;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;II - desde a posse:<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;a) ser proprietários, controladores 
    ou diretores de empresa que goze de favor decorrente de contrato com pessoa 
    jurídica de direito público ou nela exercer função remunerada;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;b) ocupar cargo ou função 
    de que sejam demissíveis "ad nutum" nas entidades referidas na alínea "a" 
    do inciso I; <BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;c) patrocinar causas em que 
    seja interessada qualquer das entidades a que se refere a alínea "a" do inciso 
    I;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;d) ser titulares de mais de 
    um cargo ou mandato público eletivo.<BR>
    <BR>
    <BR>
    Art. 37. Perderá o mandato o Vereador:<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;I - que infringir qualquer das proibições estabelecidas 
    no artigo anterior;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;II - cujo procedimento for declarado incompatível 
    com o decoro parlamentar;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;III - que deixar de comparecer, em cada sessão legislativa, 
    à terça parte das sessões ordinárias da Câmara, salvo caso de licença ou de 
    missão oficial autorizada;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;IV - que perder ou tiver suspensos os direitos políticos;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;V - quando o decretar a Justiça Eleitoral, nos casos 
    previstos em lei;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;VI - que sofrer condenação criminal em sentença transitada 
    em julgado;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;VII - que deixar de residir no Município, salvo se 
    investido em cargo em comissão dos governos Federal e Estadual ou na função 
    de representante classista;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;VIII - que deixar de tomar posse, sem motivo justificado, 
    dentro do prazo estabelecido nesta Lei Orgânica. <BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;§ 1º Extingue-se o mandato, 
    e assim será declarado pelo Presidente da Câmara, quando ocorrer falecimento 
    ou renúncia por escrito do Vereador.<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;§ 2º - Nos casos dos incisos 
    I, II, III, VI e VII deste artigo, a perda do mandato será decidida pela Câmara, 
    por voto secreto e maioria absoluta, mediante provocação da Mesa ou de Partido 
    Político representado na Câmara, assegurada ampla defesa.<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;§ 3º - Nos casos dos incisos 
    III, IV, V, VIII, a perda do mandato será declarada pela Mesa da Câmara, de 
    ofício ou mediante provocação de qualquer Vereador ou de partido representado 
    na Câmara, assegurada ampla defesa. <BR>
