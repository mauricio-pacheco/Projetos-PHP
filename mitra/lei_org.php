    </font><font size="1"><center>
      <font face="Verdana, Arial, Helvetica, sans-serif"><b>Seção IV<BR>
      Das Atribuições do Prefeito</b>
    </font></center>
    <font face="Verdana, Arial, Helvetica, sans-serif"><BR>
    <BR>
    </font></font>
  <p align="justify"><font size="1" face="Verdana, Arial, Helvetica, sans-serif"> Art. 51. Compete Privativamente ao Prefeito:<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;I - representar o município em juízo e fora dele; 
    <BR>
    &nbsp;&nbsp;&nbsp;&nbsp;II - exercer a direção superior da Administração Municipal, 
    nomear e exonerar os Secretários Municipais e seus assessores, os diretores 
    de autarquias e fundações, instituídas e mantidas majoritariamente pelo Município; 
    <BR>
    &nbsp;&nbsp;&nbsp;&nbsp;III - Iniciar o processo legislativo, na forma e nos 
    casos previstos nesta Lei Orgânica;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;IV - sancionar, promulgar e fazer publicar as leis, 
    bem como expedir decretos e regulamentos para sua fiel execução; <BR>
    &nbsp;&nbsp;&nbsp;&nbsp;V - vetar projetos de lei, total ou parcialmente;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;VI - declarar a utilidade ou necessidade pública ou 
    o interesse social de bens para fins de desapropriação ou servidão administrativa;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;VII - expedir atos próprios de sua atividade administrativa;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;VIII - aprovar projetos de edificações, planos de 
    loteamento, arruamento e zoneamento urbano ou para fins urbanos; <BR>
