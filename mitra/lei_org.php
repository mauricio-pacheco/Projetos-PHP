    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;c) as criações artísticas, 
    científicas e tecnológicas;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;d) as obras, objetos, monumentos 
    naturais e paisagens, documentos, edificações e demais espaços públicos privados 
    destinados às manifestações políticas, artísticas e culturais. <BR>
    <BR>
    Art. 196. O Poder Público Municipal, com a colaboração da comunidade, do Estado 
    e da União, protegerá o patrimônio cultural, por meio de inventários, registros, 
    tombamentos, desapropriações e outras formas de preservação.<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;§ 1º Os proprietários de bens de qualquer natureza, 
    tombados pelo Município, receberão incentivos para sua preservação e conservação, 
    conforme forem definidos em lei.<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;§ 2º Os danos e ameaças ao patrimônio cultural serão 
    punidos na forma de lei. <BR>
    <BR>
    Art. 197. O Município propiciará, em colaboração com o Estado e a União, o 
    acesso às obras de arte, com a exposição destas em locais públicos, e incentivará 
    a instalação e manutenção de bibliotecas na sede do Município e dos distritos. 
