  <p align="justify"><font size="1" face="Verdana, Arial, Helvetica, sans-serif"> Art. 39. O Vereador poderá licenciar-se:<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;I - por motivos de saúde, devidamente comprovado;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;II - para tratar de interesse particular, desde que 
    o período de licença não seja superior a cento e vinte dias por sessão legislativa;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;III - quando investido no cargo em comissão dos governos 
    Federal e Estadual, ou em cargo de direção em entidade classista.<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;§ 1º Nos casos do inciso II, 
    não poderá o Vereador reassumir antes que se tenha escoado o prazo de sua 
    licença, que será de, no mínimo, sete dias.<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;§ 2º No caso do inciso I, 
    o Vereador não sofrerá prejuízo nem no mandato nem no seu subsídio e será 
    considerado em licença pela Mesa a partir do décimo quinto dia, observada 
    a Lei Federal. (Redação dada pela Emenda nº 09, de 27-12-2001)<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;§ 3º O Vereador, para ser 
    investido no cargo de Secretário Municipal ou equivalente, deverá licenciar-se, 
    podendo optar pelo subsídio da Vereança. (Redação dada pela Emenda nº 09, 
    de 27.12.2001)<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;§ 4º O afastamento do Vereador 
    para o desempenho de missões temporárias, de interesse do Município, não será 
    considerado como de licença, fazendo o Vereador jus a remuneração estabelecida. 
    <BR>
