    &nbsp;&nbsp;&nbsp;&nbsp;XXX - estabelecer tarifas dos serviços públicos, inclusive 
    de táxi; <BR>
    &nbsp;&nbsp;&nbsp;&nbsp;XXXI - conceder, entre outras, licença para:<BR>
