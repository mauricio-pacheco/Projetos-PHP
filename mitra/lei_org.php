  <p align="justify"><font size="1" face="Verdana, Arial, Helvetica, sans-serif"> Art. 201. O Município instituirá política 
    municipal de turismo e definirá as diretrizes a observar nas ações públicas 
    e privadas, com vista a promover e incentivar o turismo como fator de desenvolvimento 
    social e econômico.<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;§ 1º Para o cumprimento do disposto neste artigo, 
    cabe ao Município, através de órgão específico, em ação conjunta com o Estado 
    e a União, promover:<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;I - o inventário e a regulamentação 
    do uso, ocupação dos bens naturais e culturais de interesse turístico;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;II - a infra-estrutura básica 
    necessária à prática do turismo, apoiando e realizando os investimentos na 
    produção, criação e qualificação dos empreendimentos, equipamentos e instalações 
    ou serviços turísticos através de linhas de créditos especiais e incentivos;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;III - implantações de ações 
    que visem ao permanente controle de qualidade dos bens e serviços turísticos;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;IV - medidas específicas para 
    o desenvolvimento dos recursos humanos para o setor;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;V - elaboração sistemática 
    de pesquisas sobre oferta e demanda turística, com análise dos fatores de 
    oscilação do mercado;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;VI - fomento ao intercâmbio 
    interno e externo, visando ao fortalecimento do espírito de fraternidade e 
    aumento do fluxo turístico. <BR>
    <BR>
    Art. 202. Esta Lei Orgânica, aprovada e assinada pelos integrantes da Câmara 
    Municipal, será por ela promulgada e entrará em vigor na data de sua publicação, 
    revogadas as disposições em contrário. <BR>
