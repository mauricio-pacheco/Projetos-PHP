    da Câmara, salvo os casos previstos no Regimento Interno. <BR>
    <BR>
    Art. 27. As sessões da Câmara serão públicas e o voto aberto, salvo deliberação 
    em contrário, tomadas pela maioria absoluta de seus membros, quando ocorrer 
    motivo relevante de preservação do decoro parlamentar e nos casos previstos 
    nesta Lei Orgânica e no Regimento Interno. <BR>
    <BR>
    Art. 28. As sessões da Câmara só poderão ser abertas com a presença mínima 
    de um terço dos Vereadores, sendo que as deliberações serão tomadas pela maioria 
    absoluta ou de dois terços, nos casos previstos no Regimento Interno. <BR>
    &nbsp;&nbsp;&nbsp;&nbsp;Parágrafo único. Considerar-se-á presente à Sessão 
    o Vereador que assinar o livro de presença e participar das votações, salvo 
    deliberação do Plenário. (Redação dada pela Emenda nº 09, de 27.12.2001) </font><BR>
