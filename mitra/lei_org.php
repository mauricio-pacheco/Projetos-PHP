    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;I - o desenvolvimento da propriedade 
    em todas as suas potencialidades, a partir da vocação e da capacidade de uso 
    do solo, levada em conta a proteção do meio ambiente;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;II - a execução de programas 
    de recuperação e conservação do solo, de reflorestamento, de irrigação, de 
    aproveitamento de cursos hídricos e de outros recursos naturais;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;III - a diversificação e rotação 
