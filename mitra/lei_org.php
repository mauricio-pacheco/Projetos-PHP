    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;IX - a garantia da utilização 
    racional dos recursos naturais.<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;§ 2º São instrumentos da política agrícola:<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;I - o ensino, a pesquisa, 
    a extensão e a assistência técnica;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;II - em caráter supletivo 
    à União e ao Estado, a eletrificação e a telefonia rurais e abastecimento 
    de água. (Redação dada pela Emenda nº 09, de 27.12.2001)<BR>
