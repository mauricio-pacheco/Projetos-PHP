    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;b) mercados, feiras e matadouros 
    locais;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;c) cemitérios e serviços funerários;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;d) iluminação pública;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;e) limpeza pública, coleta 
    domiciliar e destinação final do lixo;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;IX - legislar sobre serviços públicos de instalações 
