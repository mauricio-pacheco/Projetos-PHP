    <font face="Verdana, Arial, Helvetica, sans-serif"><BR>
    <BR>
    Art. 7º Compete ao Município:<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;I - organizar-se administrativamente, observada a 
    legislação federal e estadual;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;II - legislar sobre assuntos de interesse local;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;III - suplementar a legislação federal e a estadual 
    no que couber;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;IV - instituir e arrecadar os tributos de sua competência, 
    bem como aplicar as suas rendas, sem prejuízo da obrigatoriedade de prestar 
    contas e publicar balancetes nos prazos fixados em lei;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;V - administrar seus bens, adquiri-los e aliená-los, 
    aceitar doações, legados e heranças e dispor de sua aplicação;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;VI - desapropriar, por necessidade ou utilidade pública, 
    ou por interesse social, nos casos previstos em lei;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;VII - instituir a guarda municipal destinada à proteção 
    de seus bens, serviços e instalações, conforme dispuser a lei;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;VIII - organizar e prestar diretamente ou sob regime 
    de concessão ou permissão, entre outros, os seguintes serviços:<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;a) transporte coletivo urbano 
