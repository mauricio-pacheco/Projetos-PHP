    <BR>
    Art. 42. O Prefeito e o Vice-Prefeito serão eleitos simultaneamente, para 
    cada legislatura, por eleição direta, em sufrágio universal e secreto. <BR>
    <BR>
    Art. 43. Vagando os cargos de Prefeito e Vice-Prefeito, far-se-á eleição até 
    noventa dias depois de aberta a última vaga.<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;Parágrafo único. Ocorrendo a vacância após cumpridos 
    três quartos do mandato do Prefeito e do Vice-Prefeito, a eleição para ambos 
    os cargos será feita, pela Câmara Municipal, até trinta dias depois da última 
    vaga. <BR>
    <BR>
    Art. 44. O Prefeito e o Vice-Prefeito tomarão posse no dia 1º de janeiro do 
    ano subsequente à eleição, em sessão solene da Câmara Municipal ou, se esta 
    não estiver reunida, perante a autoridade judiciária competente, ocasião em 
    que prestarão o compromisso, nos termos do parágrafo 1º do art. 14, desta 
    Lei Orgânica.<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;§ 1º Se até o dia 10 de janeiro o Prefeito ou o Vice-Prefeito, 
