    as laterais das estradas vicinais que atravessarem ou atingirem o imóvel onde 
    trabalha e zelar pela desobstrução de sarjetas e bueiros, através de tempo 
    gratuito de serviços de máquinas, proporcional à colaboração prestada, conforme 
    disposição em lei ordinária. (Redação dada pela Emenda nº 09, de 27.12.2001) 
