    cidadão:<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;I - liberdade na criação e expressão artísticas;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;II - acesso à educação artística e ao desenvolvimento 
