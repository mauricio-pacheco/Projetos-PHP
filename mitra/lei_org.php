    caráter e uso coletivo;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;X - manter, com a cooperação técnica e financeira 
    da União e do Estado, programas de educação pré-escolar e ensino fundamental;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;XI - prestar, com a cooperação técnica e financeira 
    da União e do Estado, serviços de atendimento à saúde da população;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;XII - promover a proteção do patrimônio histórico, 
    cultural, artístico e paisagístico local, observada a legislação e a ação 
    fiscalizadora federal e estadual;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;XIII - promover a cultura e a recreação;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;XIV - fomentar a produção agropecuária, a agroindústria 
    e demais atividades econômicas; (Redação dada pela Emenda nº 09, de 27.12.2001)<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;XV - prestar serviços de assistência social;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;XVI - realizar atividades de defesa civil, inclusive 
    a de combate a incêndios e prevenção de acidentes naturais em coordenação 
    com a União e o Estado;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;XVII - promover, no que couber, adequado ordenamento 
    territorial, mediante planejamento e controle do uso, do parcelamento e da 
    ocupação do solo urbano;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;XVIII - organizar o quadro e estabelecer o regime 
    jurídico de seus servidores;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;XIX - normatizar a preservação e controle do meio 
    ambiente;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;XX - disciplinar os serviços de carga e descarga e 
    a fixação de tonelagem máxima permitida;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;XXI - estabelecer servidões administrativas necessárias 
    à realização de seus serviços;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;XXII - regulamentar a utilização das praças e logradouros 
    públicos;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;XXIII - sinalizar as vias públicas municipais e as 
    zonas de silêncio;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;XXIV - regulamentar e fiscalizar a instalação e funcionamento 
    dos elevadores;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;XXV - fixar feriados municipais bem como o horário 
    de funcionamento de estabelecimentos comerciais, industriais e de prestação 
    de serviços;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;XXVI - interditar edificações em ruínas ou em condições 
    de insalubridade e fazer demolir construções que ameacem a segurança coletiva 
    e a paisagem urbana;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;XXVII - legislar sobre apreensão, depósito e venda 
    de bens móveis, quando transgredirem normas municipais;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;XXVIII - elaborar e executar o Plano Diretor;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;XXIX - executar, entre outras, obras de:<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;a) abertura, pavimentação 
    e conservação de vias;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;b) drenagem pluvial;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;c) construção e conservação 
    de estradas, parques, jardins e hortos florestais;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;d) construção e conservação 
