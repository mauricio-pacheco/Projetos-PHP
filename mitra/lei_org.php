    próprio. <BR>
    &nbsp;&nbsp;&nbsp;&nbsp;§ 4º O Vice-Prefeito, além de outras atribuições que 
    lhe forem conferidas por lei, poderá auxiliar o Prefeito sempre que por ele 
    convidado para missões especiais, o substituirá nos casos de licença e o sucederá 
    no caso de vacância do cargo.<BR>
    <BR>
    Art. 45. Em caso de impedimento do Prefeito e do Vice-Prefeito, ou vacância 
    dos respectivos cargos, será chamado ao exercício do cargo de Prefeito, o 
    Presidente da Câmara Municipal.<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;Parágrafo único. A recusa do Presidente em assumir 
    o cargo implicará em perda do mandato que ocupa na Mesa Diretora. <BR>
