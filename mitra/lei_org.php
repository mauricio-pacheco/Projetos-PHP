    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;V - o incentivo à agroindústria;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;VI - a implantação de cinturões 
    verdes na periferia da cidade;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;VII - o oferecimento de meios 
    para assegurar ao pequeno produtor e trabalhador rural condições de trabalho 
