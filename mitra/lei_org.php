    dominiais, dependerá de lei e de licitação e far-se-á mediante contrato por 
    prazo determinado, sob pena de nulidade do ato. <BR>
    &nbsp;&nbsp;&nbsp;&nbsp;§ 1º A licitação poderá ser dispensada nos casos permitidos 
    na legislação aplicável.<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;§ 2º A permissão, que poderá incidir sobre qualquer 
    bem público, será feita mediante licitação a título precário e por decreto.<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;§ 3º A autorização, que poderá incidir sobre qualquer 
    bem público, será feita por portaria, para atividades ou usos específicos 
    e transitórios. <BR>
    <BR>
    Art. 132. A concessão administrativa de bens públicos de uso comum somente 
    pode ser outorgada para finalidades escolares de assistência social ou turística, 
    mediante autorização legislativa. </font><BR>
