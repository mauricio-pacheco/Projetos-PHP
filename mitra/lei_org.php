    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;e) prestação dos serviços 
    de táxi;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;XXXII - suspender ou cassar as licenças previstas 
    no item anterior quando incorrerem em transgressões;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;XXXIII - disciplinar a localização dos pontos de táxi 
    e paradas de ônibus. <BR>
    <BR>
    Art. 8º Compete ao Município, no que couber, concorrentemente com a União 
    ou com o Estado, nos limites da lei:<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;I - realizar programas de alfabetização, de acesso 
    à cultura, à educação e à ciência;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;II - proteger o meio ambiente, combater a poluição 
    em qualquer de suas formas, preservar as florestas, a fauna e a flora;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;III - fiscalizar a conservação, o comércio e o transporte 
    dos gêneros alimentícios destinados ao abastecimento público; <BR>
    &nbsp;&nbsp;&nbsp;&nbsp;IV - desenvolver programas de construção de moradias 
    e a melhoria das condições habitacionais e do saneamento básico;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;V - combater as causas da pobreza e os fatores de 
    marginalização, promovendo a integração social dos setores desfavorecidos;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;VI - registrar, acompanhar e fiscalizar as concessões 
    de direitos de pesquisa e exploração de recursos hídricos e minerais no território 
    do Município;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;VII - estabelecer e implantar uma política de educação 
    para a segurança do trânsito;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;VIII - abrir e conservar estradas e executar os serviços 
    públicos;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;IX - promover a defesa vegetal e animal, a extinção 
    de insetos e animais daninhos;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;X - amparar a maternidade, a infância e os desvalidos;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;XI - estimular a educação e a prática desportiva;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;XII - proteger a juventude contra toda a exploração, 
    bem como contra os fatores que possam conduzi-la ao abandono material, moral 
    e intelectual;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;XIII - incentivar o comércio, a indústria, a agricultura, 
    a agroindústria, o turismo e outros serviços, visando ao desenvolvimento econômico; 
    (Redação dada pela Emenda nº 09, de 27.12.2001)<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;XIV - disciplinar o cultivo e corte de árvores, visando 
    à saúde, à segurança das pessoas e a harmonia paisagística;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;XV - regulamentar e exercer outras atribuições não 
    vedadas pelas Constituições Federal e Estadual.<BR>
