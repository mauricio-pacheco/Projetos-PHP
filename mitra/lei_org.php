    &nbsp;&nbsp;&nbsp;&nbsp;§ 1º Os convênios podem visar à realização de obras 
    ou à exploração de serviços públicos de interesse comum.<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;§ 2º Através de convênios ou consórcios com outros 
    municípios, podem ser criadas outras entidades intermunicipais de interesse 
    comum, aprovadas por leis dos municípios participantes.<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;§ 3º É permitido ao Município, através de convênios 
    com o Estado, delegar e receber em delegação a prestação de serviços públicos, 
    desde que assegurados os recursos para a sua execução. <BR>
