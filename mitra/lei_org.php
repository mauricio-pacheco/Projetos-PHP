  <p align="justify"><font size="1" face="Verdana, Arial, Helvetica, sans-serif">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Os 
    Vereadores da Câmara Municipal de Frederico Westphalen, reunidos em Assembléia, 
    no uso das atribuições conferidas pela Constituição Federal, afirmando a autonomia 
    política e administrativa de que é investido o Município, parte integrante 
    da Federação Brasileira, invocando a proteção de Deus, promulgam a seguinte 
    LEI ORGÂNICA MUNICIPAL: <BR>
