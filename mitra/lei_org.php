    &nbsp;&nbsp;&nbsp;&nbsp;I - impedir o funcionamento regular da Câmara Municipal;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;II - impedir o exame de documentos por parte de Comissão 
