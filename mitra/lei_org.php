    prejudiciais ao meio ambiente e à saúde; <BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;VI - proteger a flora, a fauna 
    e a paisagem natural, vedadas as práticas que coloquem em risco sua função 
    ecológica e paisagística, provoquem extinção de espécie ou submetam os animais 
    a crueldade.<BR>
