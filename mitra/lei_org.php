    &nbsp;&nbsp;&nbsp;&nbsp;IV - incentivar a publicação de obras e pesquisas 
    no campo da educação;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;V - recensear os educandos para o ensino fundamental, 
    fazendo-lhes a chamada anualmente.<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;§ 1º Transcorridos dez dias 
    úteis do pedido de vaga, incorrerá em responsabilidade administrativa o Prefeito 
    que não garantir ao interessado, devidamente articulado, o acesso à escola 
    fundamental. <BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;§ 2º A comprovação do cumprimento 
    do dever de freqüência obrigatória dos alunos do ensino fundamental, será 
    feita por meio de instrumentos apropriados e regulados pelo Conselho Municipal 
    de Educação. <BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;§ 3º O não oferecimento do 
    ensino obrigatório pelo Poder Público Municipal ou sua oferta irregular, importa 
    em responsabilidade da autoridade competente. <BR>
    <BR>
    Art. 183. Será instituído por lei o Sistema Municipal de Ensino.<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;§ 1º O Sistema Municipal de Ensino compreende a integração 
    de órgãos educacionais, escolas e estabelecimentos congêneres, pertencentes 
    à rede pública municipal, existentes na área geográfica do Município, sob 
    princípios e normas comuns que lhes assegurem a necessária unidade.<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;§ 2º As escolas da rede privada de ensino fundamental 
    e pré-escolar poderão optar em pertencer ao Sistema Municipal de Ensino. <BR>
    <BR>
    Art. 184. Os recursos públicos do Município serão destinados às escolas públicas 
    municipais, podendo ser dirigidos a escolas comunitárias ou filantrópicas, 
    definidas em lei, desde que:<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;I - comprovem finalidade não lucrativa e apliquem 
    seus excedentes financeiros em educação;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;II - assegurem a destinação de seu patrimônio a outra 
    escola comunitária, filantrópica ou ao Poder Público, no caso de encerramento 
    de suas atividades.<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;§ 1º A lei disciplinará os 
    critérios e as formas de concessão e de fiscalização, pela comunidade, das 
    entidades mencionadas no "caput", a fim de verificar o cumprimento dos requisitos 
    dos incisos I e II.<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;§ 2º (Revogado pela Emenda 
    nº 09, de 27.12.2001, cuja redação havia sido alterada pela de nº 05, de 11.12.1996) 
    <BR>
    <BR>
    Art. 185. O Município aplicará, anualmente, nunca menos de vinte e cinco por 
    cento da receita resultante de impostos e das transferências recebidas do 
    Estado e da União na manutenção e no desenvolvimento do ensino fundamental 
    e na Educação Infantil. (Redação dada pela Emenda nº 09, de 27.12.2001)<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;§ 1º O Município complementará o ensino público municipal 
    com programas permanentes e gratuitos de material didático.<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;§ 2º Os programas suplementares de alimentação e assistência 
    à saúde serão mantidos com recursos financeiros constantes do percentual previsto 
    no "caput" deste artigo. <BR>
    <BR>
    Art. 186. O Governo Municipal, anualmente, publicará relatório da execução 
    financeira, da despesa em educação, por fonte de recursos, discriminando os 
    gastos mensais, enviando cópia do mesmo ao Conselho Municipal de Educação. 
    <BR>
    <BR>
    Art. 187. As escolas públicas municipais que possuírem cinco ou mais professores 
    escolherão o Diretor, mediante eleição direta, pela comunidade escolar, na 
    forma da lei.<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;Parágrafo único. O Diretor de escola pública municipal, 
    com até quatro professores, será designado pelo Prefeito Municipal, ouvida 
    a comunidade escolar. <BR>
    <BR>
    Art. 188. O Plano Municipal de Educação plurianual, será elaborado pela Secretaria 
    Municipal de Educação, juntamente com os Conselhos Escolares, devendo ser 
    aprovado pelo Conselho Municipal de Educação.<BR>
    &nbsp;&nbsp;&nbsp;&nbsp; Parágrafo único . O Plano Municipal de Educação deverá 
    priorizar o atendimento ao ensino fundamental, ao pré-escolar e à erradicação 
    do analfabetismo. <BR>
    <BR>
    Art. 189. O ensino religioso, de matrícula facultativa, constará dos horários 
    normais das escolas públicas do ensino fundamental e médio. <BR>
    <BR>
    Art. 190. Será estimulado o pluralismo de idiomas nas escolas, na medida em 
    que atenda a uma demanda significativa de grupos interessados ou de origens 
    étnicas diferentes. <BR>
    <BR>
    Art. 191. O Município estimulará e desenvolverá programas sobre cooperativismo, 
    sindicalismo, saúde, meio ambiente e educação para o trânsito. <BR>
    <BR>
    Art. 192. É assegurado o Plano de Carreira do Magistério Público Municipal, 
    garantida a valorização da qualificação profissional do professor, independente 
    do nível em que atue, inclusive mediante a fixação de piso salarial. <BR>
    <BR>
    Art. 193. É assegurado aos pais, professores, alunos e funcionários organizarem-se 
    em todos os estabelecimentos municipais de ensino, através de associações, 
    grêmios e outras formas. <BR>
    <BR>
    Art. 194. O Município manterá um sistema de bibliotecas escolares na rede 
    pública Municipal. </font><BR>
