    às proibições e incompatibilidades previstas para os Vereadores, no que for 
    aplicável. <BR>
    <BR>
    Art. 47. São infrações político-administrativas do Prefeito e do Vice-Prefeito, 
    sujeitas ao julgamento pela Câmara Municipal e sancionadas com a cassação 
