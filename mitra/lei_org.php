  <p align="justify"><font size="1" face="Verdana, Arial, Helvetica, sans-serif"> Art. 1º O Município de Frederico Westphalen, 
    pessoa jurídica de direito público interno, unidade territorial do Estado 
    do Rio Grande do Sul, República Federativa do Brasil, é dotado de autonomia 
    política, administrativa, financeira e legislativa, nos termos assegurados 
    pela Constituição da República, pela Constituição do Estado e por esta Lei 
    Orgânica Municipal. <BR>
    <BR>
    Art. 2º É mantido o atual território do Município e permitida a divisão em 
    distritos e sub-distritos, criados, organizados e extintos por lei municipal, 
    observada a legislação estadual. <BR>
    <BR>
    Art. 3º São símbolos do Município o Brasão, a Bandeira e o Hino, representativos 
    de sua cultura e história, e outros a serem instituídos por lei. <BR>
    <BR>
    Art. 4º A cidade de Frederico Westphalen é a sede do Município. <BR>
    <BR>
    Art. 5º Ao Município é vedado:<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;I - estabelecer cultos religiosos ou igrejas, subvencioná-los, 
    impedir-lhes o funcionamento ou manter com eles ou seus representantes relações 
    de dependência ou aliança, ressalvada, na forma da lei, a colaboração de interesse 
    público;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;II - recusar fé aos documentos públicos;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;III - criar distinção entre brasileiros;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;IV - contrair empréstimos externos sem prévia autorização 
    do Senado Federal;<BR>
    &nbsp;&nbsp;&nbsp;&nbsp;V - instituir ou aumentar tributos sem que lei anterior 
    o estabeleça e a vigir no exercício seguinte.<BR>
    <BR>
    Art. 6º São bens do Município os que atualmente lhe pertencem e os que lhe 
    vierem a ser atribuídos. <BR>
    <BR>
