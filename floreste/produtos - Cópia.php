          <p>Sua madeira de excelente qualidade é utilizada na fabricação de  
            móveis, portas, carvão, MDF, aglomerados, laminados e moradias. 
            Uma floresta de Acacia mangium trará grande segurança aos 
            empresários rurais, por atender à reposição florestal exigida por lei,  
            por proteger suas terras da erosão, por garantir a manutenção das  
            nascentes de água, por constituir patrimônio de fácil e rápida  
