            Silvipastoril), podem reduzir a erosão, melhorar a conservação da  
            água, reduzir a necessidade de fertilizantes minerais, capturar e  
            fixar carbono, diversificar a produção, aumentar a renda e a <br />
            biodiversidade, melhorar o conforto dos animais.</p>
