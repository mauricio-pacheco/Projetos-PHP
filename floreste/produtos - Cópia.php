          <p><strong>Muda de Sansão do campo (Mimosa caesalpineafolia)</strong></p>
          <p>Espécie nativa da região nordeste do Brasil, é um arbusto de rápido 
            crescimento, que apresenta vantagens que o tornam ideal para a  
            formação de cerca viva. Produz espinhos semelhantes aos da  
            roseira, funcionando como uma barreira contra invasores, forma  
            uma proteção contra a poeira das estradas e também age como  
            quebra ventos, é uma planta perene que necessita de pouquíssima 
            manutenção e, dependendo da situação, dispensa inclusive as  
            podas, suas pequenas flores acrescentam valor ornamental à  
            cerca viva, a espécie é muito rústica e resistente, adapta-se a  
            quase todos os tipos de solo, não sendo muito exigente.  
            Para obtenção de uma cerca viva com perfeição e beleza, o plantio  
            do sansão do campo deve ser feito com 10 cm de espaçamento  
            entre as plantas. Desta forma, os troncos se encostarão formando  
            uma espécie de &quot;muralha&quot; capaz de conter com eficiência pessoas, 
            aves e animais de pequeno e grande porte.</p>
